/

COMMENT ON COLUMN SDCALENDAR.SDCCONTENT IS '일정내용'
/

COMMENT ON COLUMN SDCALENDAR.SDCSARTDATE IS '일정시작날짜'
/

COMMENT ON COLUMN SDCALENDAR.SDCENDDATE IS '일정끝날짜'
/

