    SDBTABNO       NUMBER            NULL, 
    SDBGROUPSEQ    NUMBER            NULL, 
    SDBGROUPNO     NUMBER            NULL, 
    SDBTITLE       VARCHAR2(200)     NULL, 
    SDUEAMIL       VARCHAR2(100)     NOT NULL, 
    SDUB           VARCHAR2(4000)    NULL, 
